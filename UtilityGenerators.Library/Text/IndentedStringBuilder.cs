    #region Close Block
    void CloseBlockCore()
    {
        if(!LastBlock.HasValue)
            return;

        DetentCore();

        var block = LastBlock.Value;

        if(block.PlaceDelimitersOnNewLine)
            AppendSingleLineCore();

        AppendCore(block.ClosingDelimiter, default);
        _ = _blocks.Pop();
    }
    public IndentedStringBuilder CloseBlock()
