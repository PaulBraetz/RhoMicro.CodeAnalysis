    public IndentedStringBuilder(IndentedStringBuilderOptions options)
    {
        Options = options;

        if(Options.PrependMarkerComment)
        {
            _ = _builder.AppendLine("// <auto-generated>");

            if(!String.IsNullOrWhiteSpace(Options.GeneratorName))
            {
                _ = _builder.Append("// This file was last generated by ")
                    .Append(Options.GeneratorName)
                    .Append(" at ")
                    .AppendLine(DateTimeOffset.Now.ToString());
            }

            _ = _builder
                .AppendLine("// The tool used to generate this code may be subject to license terms;")
                .AppendLine("// this generated code is however not subject to those terms, instead it is")
                .AppendLine("// subject to the license (if any) applied to the containing project.")
                .AppendLine("// </auto-generated>");
        }

        if(Options.PrependWarningDisablePragma)
            _ = _builder.AppendLine("#pragma warning disable");

        if(Options.PrependNullableEnable)
            _ = _builder.AppendLine("#nullable enable");

        Comment = new(this);
        Operators = new(this);
    }

    public IndentedStringBuilder() : this(IndentedStringBuilderOptions.Default)
    { }

    #region Fields & Properties
